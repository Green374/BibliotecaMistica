    private void jBtnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAceptarActionPerformed
        //atención debemos chequear que no existe el código para no repetirlo
        if (checkAptoModificar()) {
            cargaDatosDesdeFormulario();
            try {
                if (JOptionPane.showConfirmDialog(null, "¿Confirma la modificación del autor " + autor + "?", "Atención", JOptionPane.YES_NO_OPTION) == 0) {
                    fachadaLogica.modificarAutor(autor);
                    JOptionPane.showMessageDialog(null, "La modificación se realizó correctamente.", "Autores", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (AutorException ex) {
                JOptionPane.showMessageDialog(null, "Atención: \n" + ex, "Autores", JOptionPane.WARNING_MESSAGE);
            }
        } else if (checkAptoIngresar()) {
            cargaDatosDesdeFormulario();
            try {
                fachadaLogica.ingresarAutor(autor);
                JOptionPane.showMessageDialog(null, "El alta se realizó correctamente.", "Autores", JOptionPane.INFORMATION_MESSAGE);
            } catch (AutorException ex) {
                JOptionPane.showMessageDialog(null, "Atención: \n" + ex, "Autores", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "No se pudo realizar ninguna tarea con autores. \n Por favor seleccione uno para su modificación o clic en limpiar para ingresar uno nuevo.", "Autores", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jBtnAceptarActionPerformed

