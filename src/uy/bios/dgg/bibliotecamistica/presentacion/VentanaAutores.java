    private void limpiarFormulario() {
        this.jTxtCodigo.setText("");
        this.jTxtNombre.setText("");
        this.jTxtApellido.setText("");
        this.jTxtNacionalidad.setText("");
    }

    private void mostrarAutor() {
        this.jTxtCodigo.setText(autor.getCodigo());
        this.jTxtNombre.setText(autor.getNombre());
        this.jTxtApellido.setText(autor.getApellido());
        this.jTxtNacionalidad.setText(autor.getNacionalidad());
    }

    private void cargaDatosDesdeFormulario() {
        autor.setCodigo(this.jTxtCodigo.getText());
        autor.setNombre(this.jTxtNombre.getText());
        autor.setApellido(this.jTxtApellido.getText());
        autor.setNacionalidad(this.jTxtNacionalidad.getText());
    }

    private boolean checkAptoBorrar() {
        //chequeo para asegurarnos que la categoria en memoria sea igual a la que se visualiza en pantalla
        if (autor.getIdautor() != null) {
            if (this.jTxtCodigo.getText().trim().equals(autor.getCodigo())) {
                return true;
            }
        }
        return false;
    }

    private boolean checkAptoModificar() {
        //chequeo para saber si es apto para realizar una modificación
        if (autor.getIdautor() != null) {
            if (!this.jTxtCodigo.getText().trim().equals(autor.getCodigo()) || !this.jTxtNombre.getText().trim().equals(autor.getNombre()) || !this.jTxtApellido.getText().trim().equals(autor.getApellido()) || !this.jTxtNacionalidad.getText().trim().equals(autor.getNacionalidad())) {
                if (!this.jTxtCodigo.getText().trim().equals("") && !this.jTxtNombre.getText().trim().equals("") && !this.jTxtApellido.getText().trim().equals("") && !this.jTxtNacionalidad.getText().trim().equals("")) {
                    return true;
                }
            }
        }
        return false;
    }

    private boolean checkAptoIngresar() {
        //chequeo para saber si es apto para realizar un nuevo ingreso
        if (autor.getIdautor() == null) {
            if (!this.jTxtCodigo.getText().trim().equals("") && !this.jTxtNombre.getText().trim().equals("") && !this.jTxtApellido.getText().trim().equals("") && !this.jTxtNacionalidad.getText().trim().equals("")) {
                return true;
            }
        }
        return false;
    }

    private void refrescaCombo() {
        Autores autores;
        this.combo.removeAllItems();
        try {
            autores = fachadaLogica.obtenerAutores();
            for (int i = 0; i < autores.getAutores().size(); i++) {
                this.combo.addItem(autores.getAutores().get(i));
                if (autores.getAutores().get(i).getIdautor().equals(this.autor.getIdautor())){ //si es el autor actual lo selecciono
                    this.combo.setSelectedIndex(i);
                }
            }
        } catch (AutorException ex) {
            JOptionPane.showMessageDialog(null, "Atención: \n" + ex, "Libros", JOptionPane.WARNING_MESSAGE);
        }
    }
