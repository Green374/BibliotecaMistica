    private void jBtnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnBuscarActionPerformed
        if (!this.jTxtCodigo.getText().trim().equals("")) {
            try {
                String codigo = this.jTxtCodigo.getText().trim();
                autor = fachadaLogica.obtenerAutor(codigo);
                if (autor.getIdautor() == null) {
                    JOptionPane.showMessageDialog(null, "No se encontró un autor con el código ingresado.", "Resultado de la búsqueda", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    mostrarAutor();
                }
            } catch (AutorException ex) {
                JOptionPane.showMessageDialog(null, ex, "Atención", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_jBtnBuscarActionPerformed

    private void jBtnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnBorrarActionPerformed
        if (checkAptoBorrar()) {
            try {
                if (JOptionPane.showConfirmDialog(null, "¿Confirma el borrado del autor " + autor + "?", "Atención", JOptionPane.YES_NO_OPTION) == 0) {
                    fachadaLogica.borrarAutor(autor);
                    this.limpiarFormulario();
                    JOptionPane.showMessageDialog(null, "El autor " + autor + " se borró correctamente.", "Baja de autor", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (AutorException ex) {
                JOptionPane.showMessageDialog(null, ex, "Atención", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "No se pudo borrar ningún autor. \n Por favor seleccione uno y vuelva a intentarlo.", "Baja de autor", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jBtnBorrarActionPerformed

