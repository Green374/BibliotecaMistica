    private void jBtnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnBuscarActionPerformed
        
        if (!this.jTxtDocumento.getText().trim().equals("")) {
            try {
                String docIdentidad = this.jTxtDocumento.getText().trim();
                socio = fachadaLogica.obtenerSocio(docIdentidad);
                if (socio.getIdSocio() == null) {
                    JOptionPane.showMessageDialog(null, "No se encontró un socio con el Documento ingresado.", "Resultado de la búsqueda", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    mostrarSocio();
                }
            } catch (SocioException ex) {
                JOptionPane.showMessageDialog(null, ex, "Atención", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_jBtnBuscarActionPerformed

