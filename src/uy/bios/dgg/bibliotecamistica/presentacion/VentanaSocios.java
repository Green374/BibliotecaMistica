    private void jBtnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAceptarActionPerformed
       if (checkAptoModificar()) {
            cargaDatosDesdeFormulario();
            try {
                if (JOptionPane.showConfirmDialog(null, "¿Confirma la modificación del socio " + socio + "?", "Atención", JOptionPane.YES_NO_OPTION) == 0) {
                    fachadaLogica.modificarSocio(socio);
                    JOptionPane.showMessageDialog(null, "La modificación se realizó correctamente.", "Socios", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (SocioException ex) {
                JOptionPane.showMessageDialog(null, "Atención: \n" + ex, "Socios", JOptionPane.WARNING_MESSAGE);
            }
        } else if (checkAptoIngresar()) {
            cargaDatosDesdeFormulario();
            try {
                fachadaLogica.ingresarSocio(socio);
                JOptionPane.showMessageDialog(null, "El alta se realizó correctamente.", "Socios", JOptionPane.INFORMATION_MESSAGE);
            } catch (SocioException ex) {
                JOptionPane.showMessageDialog(null, "Atención: \n" + ex, "Socios", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "No se pudo realizar ninguna tarea con Socios. \n Por favor seleccione uno para su modificación o clic en limpiar para ingresar uno nuevo.", "Socios", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jBtnAceptarActionPerformed

    private void jBtnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnBorrarActionPerformed
        if (checkAptoBorrar()) {
            try {
                if (JOptionPane.showConfirmDialog(null, "¿Confirma el borrado del socio " + socio + "?", "Atención", JOptionPane.YES_NO_OPTION) == 0) {
                    fachadaLogica.borrarSocio(socio);
                    this.limpiarFormulario();
                    JOptionPane.showMessageDialog(null, "El socio " + socio + " se borró correctamente.", "Baja de autor", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (SocioException ex) {
                JOptionPane.showMessageDialog(null, ex, "Atención", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "No se pudo borrar ningún socio. \n Por favor seleccione uno y vuelva a intentarlo.", "Baja de autor", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jBtnBorrarActionPerformed

